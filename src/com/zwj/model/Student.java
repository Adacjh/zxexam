package com.zwj.model;

public class Student {
	private int stuId;
	private String stuName;
	private String stuPassword;
	private int stuSex;
	private String stuPhone;
	private String stuTime;
	private int stuState;
	private String stuValue;
	public int getStuId() {
		return stuId;
	}
	public void setStuId(int stuId) {
		this.stuId = stuId;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	public String getStuPassword() {
		return stuPassword;
	}
	public void setStuPassword(String stuPassword) {
		this.stuPassword = stuPassword;
	}
	public int getStuSex() {
		return stuSex;
	}
	public void setStuSex(int stuSex) {
		this.stuSex = stuSex;
	}
	public String getStuPhone() {
		return stuPhone;
	}
	public void setStuPhone(String stuPhone) {
		this.stuPhone = stuPhone;
	}
	public String getStuTime() {
		return stuTime;
	}
	public void setStuTime(String stuTime) {
		this.stuTime = stuTime;
	}
	public int getStuState() {
		return stuState;
	}
	public void setStuState(int stuState) {
		this.stuState = stuState;
	}
	public String getStuValue() {
		return stuValue;
	}
	public void setStuValue(String stuValue) {
		this.stuValue = stuValue;
	}
}
